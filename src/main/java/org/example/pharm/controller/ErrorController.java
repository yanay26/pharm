package org.example.pharm.controller;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;

/**
 * Контроллер для обработки ошибок, возникающих при попытке доступа к защищённым или несуществующим страницам.
 * <p>
 * Этот контроллер перехватывает ошибки доступа (403) и ошибки "страница не найдена" (404),
 * отображая соответствующие страницы с сообщениями об ошибке.
 */
@Controller
public class ErrorController {

    /**
     * Обрабатывает ошибку доступа (код 403).
     * <p>
     * При попытке доступа к странице, для которой у пользователя нет прав, отображается страница ошибки с сообщением
     * о недостаточных правах для доступа.
     *
     * @param model модель, в которую добавляется сообщение об ошибке
     * @return имя шаблона для отображения страницы ошибки 403
     */
    @GetMapping("/403")
    public String handleAccessDenied(Model model) {
        model.addAttribute("errorMessage", "У вас нет прав для доступа к этой странице.");
        return "error/403";  // Шаблон страницы ошибки 403
    }

    /**
     * Обрабатывает ошибку "страница не найдена" (код 404).
     * <p>
     * При попытке доступа к несуществующей странице отображается страница ошибки с сообщением
     * о том, что страница не была найдена.
     *
     * @param model модель, в которую добавляется сообщение об ошибке
     * @return имя шаблона для отображения страницы ошибки 404
     */
    @GetMapping("/404")
    public String handleNotFound(Model model) {
        model.addAttribute("errorMessage", "Страница не найдена.");
        return "error/404";  // Шаблон страницы ошибки 404
    }

    /**
     * Обрабатывает ошибку сервера (код 500).
     * <p>
     * При возникновении ошибки на сервере (например, исключение в коде), отображается страница ошибки с сообщением
     * о внутренней ошибке на сервере.
     *
     * @param model модель, в которую добавляется сообщение об ошибке
     * @return имя шаблона для отображения страницы ошибки 500
     */
    @GetMapping("/500")
    public String handleServerError(Model model) {
        model.addAttribute("errorMessage", "Произошла внутренняя ошибка на сервере.");
        return "error/500";  // Шаблон страницы ошибки 500
    }
}




